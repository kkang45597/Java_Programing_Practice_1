package javaprogrammingunitexam0722;

public class Person {
	
	String name;
	String email;
	
	public String getterName() {
		return name;
	}
	public String getterEmail() {
		return email;
	}
	
	public void setter(String name, String email) {
		this.name = name;
		this.email = email;
	}
	
	public String toString() {
		return email;
		
	}

}
